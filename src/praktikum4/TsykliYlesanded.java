package praktikum4;

public class TsykliYlesanded {

	public static void main(String[] args) {
		for (int i = 0; i < 10; i++) {
			System.out.print(10 - i + " ");
		}
		System.out.println();

		for (int i = 0; i <= 10; i += 2) {
			System.out.print(i + " ");
		}
		System.out.println();

		for (int i = 0; i <= 30; i += 3) {
			System.out.print(30 - i + " ");
		}

	}

}
